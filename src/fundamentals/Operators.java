package fundamentals;

public class Operators {


    public static void main(String[] args) {

        int k = 10;

        System.out.println(k != 0);
        System.out.println(k == 10);
        System.out.println((k <= 10 && k >= 10));
        System.out.println((k <= 10 || k >= 10));
        System.out.println(!(k <= 9));

    }
}
