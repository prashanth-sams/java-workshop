package fundamentals;

public class IfElseIf {


    public static void main(String[] args) {

        int k = 39;

        if (k <= 10) {
            System.out.println(k + " lesser than or equal to 10");
        } else if (k <= 20) {
            System.out.println(k + " lesser than or equal to 20");
        } else if (k <= 30) {
            System.out.println(k + " lesser than or equal to 30");
        } else if (k <= 40) {
            System.out.println(k + " lesser than or equal to 40");
        } else if (k <= 50) {
            System.out.println(k + " lesser than or equal to 50");
        } else {
            System.out.println(k + " greater than 50");
        }
    }
}
